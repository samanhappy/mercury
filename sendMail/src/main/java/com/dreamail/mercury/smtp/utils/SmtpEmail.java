package com.dreamail.mercury.smtp.utils;

import java.util.List;


public class SmtpEmail {

	private String from;// 发件人
	private User user;
	private String proxyName;
    private String proxyPassword;
    private String sendHost;
    private String sendPort;
    private String sendProtocolType;
	private String replyTo;// 回复
	private String to;// 收件人
	private String cc;// 抄送
	private String subject;// 标题
	private String sendTime;// 邮件时间
	private String bcc;// 秘送
	private String messageId;// 邮件唯一编号
	private SmtpEmailBody body;
	private List<SmtpEmailattachment> attach;// 邮件附件
	
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	public String getProxyName() {
		return proxyName;
	}
	public void setProxyName(String proxyName) {
		this.proxyName = proxyName;
	}
	public String getProxyPassword() {
		return proxyPassword;
	}
	public void setProxyPassword(String proxyPassword) {
		this.proxyPassword = proxyPassword;
	}
	public String getSendHost() {
		return sendHost;
	}
	public void setSendHost(String sendHost) {
		this.sendHost = sendHost;
	}
	public String getSendPort() {
		return sendPort;
	}
	public void setSendPort(String sendPort) {
		this.sendPort = sendPort;
	}
	public String getSendProtocolType() {
		return sendProtocolType;
	}
	public void setSendProtocolType(String sendProtocolType) {
		this.sendProtocolType = sendProtocolType;
	}
	public String getReplyTo() {
		return replyTo;
	}
	public void setReplyTo(String replyTo) {
		this.replyTo = replyTo;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public String getCc() {
		return cc;
	}
	public void setCc(String cc) {
		this.cc = cc;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getSendTime() {
		return sendTime;
	}
	public void setSendTime(String sendTime) {
		this.sendTime = sendTime;
	}
	public String getBcc() {
		return bcc;
	}
	public void setBcc(String bcc) {
		this.bcc = bcc;
	}
	public String getMessageId() {
		return messageId;
	}
	public void setMessageId(String messageId) {
		this.messageId = messageId;
	}
	public SmtpEmailBody getBody() {
		return body;
	}
	public void setBody(SmtpEmailBody body) {
		this.body = body;
	}
	public List<SmtpEmailattachment> getAttach() {
		return attach;
	}
	public void setAttach(List<SmtpEmailattachment> attach) {
		this.attach = attach;
	}
	
}
