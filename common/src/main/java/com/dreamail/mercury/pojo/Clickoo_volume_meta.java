package com.dreamail.mercury.pojo;

public class Clickoo_volume_meta {

	private long id;
	
	private String vid;
	
	private String name;
	
	public Clickoo_volume_meta(){}

	public Clickoo_volume_meta(long id, String name, String vid) {
		super();
		this.id = id;
		this.vid = vid;
		this.name = name;
	}
	
	
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getVid() {
		return vid;
	}

	public void setVid(String vid) {
		this.vid = vid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
