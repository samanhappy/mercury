package com.dreamail.mercury.domain;


public class WebUser {
	private long uid;//用户ID
	
	private String encryptmode;
	
	private String name;//用户名
	
	private String passwd;//用户密码
        
    private String gender;//性别，男为“1”，女为“0”
    
    private String birthday;//生日
    
    private String declaration;//说明
    
    private String IMEI;//手机唯一标识
    
    private String devicemodel;//手机配置
    
    private String signature;//个性签名
    
    private long roleid;// 角色

    private String isPushemail;//是否推送邮件
    
    private String timestamp;
    
    private String sign;

	public String getIsPushemail() {
		return isPushemail;
	}

	public String getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

	public void setIsPushemail(String isPushemail) {
		this.isPushemail = isPushemail;
	}

	public long getRoleid() {
		return roleid;
	}

	public void setRoleid(long roleid) {
		this.roleid = roleid;
	}

	public String getSignature() {
		return signature;
	}

	public void setSignature(String signature) {
		this.signature = signature;
	}

	public String getDevicemodel() {
		return devicemodel;
	}

	public void setDevicemodel(String devicemodel) {
		this.devicemodel = devicemodel;
	}

	public long getUid() {
		return uid;
	}

	public void setUid(long uid) {
		this.uid = uid;
	}

	public String getEncryptmode() {
		return encryptmode;
	}

	public void setEncryptmode(String encryptmode) {
		this.encryptmode = encryptmode;
	}

	public String getName() {
		return name;
	}

	public void setName(String username) {
		this.name = username;
	}
	
	public String getPasswd() {
		return passwd;
	}

	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	
	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getBirthday() {
		return birthday;
	}

	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}

	public String getDeclaration() {
		return declaration;
	}

	public void setDeclaration(String declaration) {
		this.declaration = declaration;
	}

	public String getIMEI() {
		return IMEI;
	}

	public void setIMEI(String iMEI) {
		IMEI = iMEI;
	}

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}
    
}
